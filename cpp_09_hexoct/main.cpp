#include <iostream>

using namespace std;

int main()
{
    cout << "Hello world!" << endl;

    int chest = 42;
    int waist = 042;
    int inseam = 0x42;

    cout << "Moisture cuts a striking figure!" << endl;
    cout << "chest  = " << chest << " (42 in decimal)" << endl;
    cout << "waist  = " << waist << " (042 in octal)" << endl;
    cout << "inseam = " << inseam << " (0x42 in hex)" << endl;

    return 0;
}
