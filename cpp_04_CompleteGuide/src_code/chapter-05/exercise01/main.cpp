#include <iostream>

using namespace std;

int main()
{
    cout << "Hello world!" << endl;
    cout << "a = " << 3/10 << endl; // 0
    cout << "b = " << 11%4 << endl; // 3
    cout << "c = " << 15/2.0 << endl; // 7.5
    cout << "d = " << 3 + 4 % 5 << endl; // 7
    cout << "e = " << 3 * 7 % 4 << endl; // 1
    cout << "f = " << 7 % 4 * 3 << endl; // 9

    return 0;
}
